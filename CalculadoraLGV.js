class Calculadora{ //clase que contendra los metodos que usaremos en la calculadora
    
    suma(num1,num2){
        return num1 + num2;
    }

    resta(num1,num2){
        return num1-num2;
    }

    multiplicacion(num1,num2){
        return num1*num2;
    }

    division(num1,num2){
        return num1/num2;
    }
}

